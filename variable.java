public class variable{
    public static void main(String[] args) {
        System.out.println("Hello world");

        //datatpes
        //int float char string long double boolean

        int age = 18;
        System.out.println(age);

        // starting of variable with alphanumeric value($),alphabets and underscore(_)
        //dont start with numbers and keywords

        float marks = 12.232f;
        System.out.println(marks);

        char name='S'; //character always in single quotes
        System.out.println(name);

        String nm="Sumit"; //string always in double quotes
        System.out.println(nm);

        boolean isValid=true;
        System.out.println(isValid);

        long bigNumber=34234382439434499l;
        System.out.println(bigNumber);
    }
}